import math

def Rescue_time_calculator(d1, d2, h, v_sand, n, th1):

    d1 = d1 * 3 # –ö—Ä–∞—Ç—á–∞–π—à–µ–µ —Ä–∞—Å—Å—Ç–æ—è–Ω–∏–µ –æ—Ç —Å–ø–∞—Å–∞—Ç–µ–ª—è –¥–æ –∫—Ä–æ–º–∫–∏ –≤–æ–¥—ã, ùëë1 (–≤ —è—Ä–¥–∞—Ö) => –≤ —Ñ—É—Ç—ã
    # d2  # –ö—Ä–∞—Ç—á–∞–π—à–µ–µ —Ä–∞—Å—Å—Ç–æ—è–Ω–∏–µ –æ—Ç —É—Ç–æ–ø–∞—é—â–µ–≥–æ –¥–æ –±–µ—Ä–µ–≥–∞, ùëë2 (–≤ —Ñ—É—Ç–∞—Ö)
    h = h * 3 # –ë–æ–∫–æ–≤–æ–µ —Å–º–µ—â–µ–Ω–∏–µ –º–µ–∂–¥—É —Å–ø–∞—Å–∞—Ç–µ–ª–µ–º –∏ —É—Ç–æ–ø–∞—é—â–∏–º, ‚Ñé (–≤ —è—Ä–¥–∞—Ö) => –≤ —Ñ—É—Ç—ã
    v_sand = v_sand * 5280 / 3600  # –°–∫–æ—Ä–æ—Å—Ç—å –¥–≤–∏–∂–µ–Ω–∏—è —Å–ø–∞—Å–∞—Ç–µ–ª—è –ø–æ –ø–µ—Å–∫—É, ùë£ùë†ùëéùëõùëë (–≤ –º–∏–ª—è—Ö –≤ —á–∞—Å) => –≤ —Ñ—É—Ç—ã –≤ –°–ï–ö–£–ù–î–£!
    # n  # –ö–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç –∑–∞–º–µ–¥–ª–µ–Ω–∏—è —Å–ø–∞—Å–∞—Ç–µ–ª—è –ø—Ä–∏ –¥–≤–∏–∂–µ–Ω–∏–∏ –≤ –≤–æ–¥–µ, ùëõ
    th1 = 39.413 # –ù–∞–ø—Ä–∞–≤–ª–µ–Ω–∏–µ –¥–≤–∏–∂–µ–Ω–∏—è —Å–ø–∞—Å–∞—Ç–µ–ª—è –ø–æ –ø–µ—Å–∫—É, ùúÉ1 (–≤ –≥—Ä–∞–¥—É—Å–∞—Ö)  
    th1_rad = th1 * (math.pi / 180)

    x = d1 * (math.tan(th1_rad))
    L1 = math.sqrt(x**2 + d1**2)
    L2 = math.sqrt((h - x)**2 + d2**2)
    t = 1 / v_sand * (L1 +  n * L2)

    return t

d1 = int(input("–í–≤–µ–¥–∏—Ç–µ –∫—Ä–∞—Ç—á–∞–π—à–µ–µ —Ä–∞—Å—Å—Ç–æ—è–Ω–∏–µ –º–µ–∂–¥—É —Å–ø–∞—Å–∞—Ç–µ–ª–µ–º –∏ –∫—Ä–æ–º–∫–æ–π –≤–æ–¥—ã, d1 (—è—Ä–¥—ã): "))
d2 = int(input("–í–≤–µ–¥–∏—Ç–µ –∫—Ä–∞—Ç—á–∞–π—à–µ–µ —Ä–∞—Å—Å—Ç–æ—è–Ω–∏–µ –æ—Ç —É—Ç–æ–ø–∞—é—â–µ–≥–æ –¥–æ –±–µ—Ä–µ–≥–∞, d2 (—Ñ—É—Ç—ã): "))
h = int(input("–í–≤–µ–¥–∏—Ç–µ –±–æ–∫–æ–≤–æ–µ —Å–º–µ—â–µ–Ω–∏–µ –º–µ–∂–¥—É —Å–ø–∞—Å–∞—Ç–µ–ª–µ–º –∏ —É—Ç–æ–ø–∞—é—â–∏–º, h (—è—Ä–¥—ã): "))
v_sand = int(input("–í–≤–µ–¥–∏—Ç–µ —Å–∫–æ—Ä–æ—Å—Ç—å –¥–≤–∏–∂–µ–Ω–∏—è —Å–ø–∞—Å–∞—Ç–µ–ª—è –ø–æ –ø–µ—Å–∫—É, v_sand (–º–∏–ª–∏ –≤ —á–∞—Å): "))
n = int(input("–í–≤–µ–¥–∏—Ç–µ –∫–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç –∑–∞–º–µ–¥–ª–µ–Ω–∏—è —Å–ø–∞—Å–∞—Ç–µ–ª—è –ø—Ä–∏ –¥–≤–∏–∂–µ–Ω–∏–∏ –≤ –≤–æ–¥–µ, n: "))
theta1 = float(input("–í–≤–µ–¥–∏—Ç–µ –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏–µ –¥–≤–∏–∂–µ–Ω–∏—è —Å–ø–∞—Å–∞—Ç–µ–ª—è –ø–æ –ø–µ—Å–∫—É, theta1 (–≥—Ä–∞–¥—É—Å—ã): "))


result = Rescue_time_calculator(d1, d2, h, v_sand, n, theta1)
print(result.__round__(1))

